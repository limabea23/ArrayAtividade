//método5: concat = Junta (concatena) dois ou mais arrays, retornando um novo array que contém todos os elementos dos arrays originais combinados em um único array.

//exemplo 1
let array1 = [9, 18, 27]; //declara a variavél
let array2 = [3, 6, 9]; //declara outra variavél
let concatenatedArray = array1.concat(array2); //cria um array que junta todos os elementos do array1 e array2
console.log(concatenatedArray); 

