//método9: slice = Retorna uma cópia superficial de uma porção do array em um novo array.

//exemplo 1
let países = ["Brasil", "México", "Autrália", "Coreia do Sul"]; //declara a variável
let slicedPaíses = países.slice(2, 3); //retorna uma cópia
console.log(slicedPaíses); //exibe a mensagem (resultado)

